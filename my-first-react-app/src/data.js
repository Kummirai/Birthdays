export const numberOfDays = (birthDate) => {
  if (new Date(birthDate).getTime() < new Date().getTime()) {
    birthDate = `${new Date().getFullYear() + 1}-${birthDate.split("-")[1]}-${
      birthDate.split("-")[2]
    }`;
    return Math.ceil(
      (new Date(birthDate).getTime() - new Date().getTime()) / 86400000
    );
  }
  return Math.ceil(
    (new Date(birthDate).getTime() - new Date().getTime()) / 86400000
  );
};

